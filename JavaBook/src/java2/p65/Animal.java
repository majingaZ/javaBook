package java2.p65;

public class Animal {
	private String name;
	private int age;
	
	public void setName(String pname) {
	name = pname;
	}
	
	public String getName() {
		return name;
	}
	
	public void setAge(Integer iage) {
	age = iage;
	}
	
	public Integer getAge() {
		return age;
	}

}
